package datatypes;

public class IntegerNotations {

    public static void main(String[] args) {
        
        int n1 = 31;        //decimal
        int n2 = 0x31;      //hexadec
        int n3 = 031;       //octal
        int n4 = 0b1001;    //binary
        
        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n3);
        System.out.println(n4);
    }
}
